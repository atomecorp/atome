# web_object example

my_tube = web('<iframe width="100%" height="180%" src="https://www.youtube.com/embed/usQDazZKWAk" frameborder="5" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>')
my_tube.x = 300
my_tube.y = 150
my_tube.drag(true)
my_tube.width = 300